public class ThirdProgramm {
    public static void main(String[] args) {
        int x, y;
        x = 10;
        y = 20;
        if (x < y) System.out.println("X меньше Y");
        x = x * 2;
        if (x == y) System.out.println("Теперь X равно Y");
        x = x * 2;
        if (x > y) System.out.println("Теперь X больше Y");
    }
}
